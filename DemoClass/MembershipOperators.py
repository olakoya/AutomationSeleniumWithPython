# Membership Operators
# This means it will check presence of an element/object/item inside a sequences (string, list, tuple, set, dictionary)
# Types of Membership Operators are
# 1.   in ==> True, False (i.e. if an element is present it will return True Value and if not it will return False Value)
# 2.  not in ==> True, False (i.e. if an element is not present it will return either True and if not it will return False Valuee)

# Special Operators

a = "Welcome" # "Welcome" is a sequence in "a" value
print("w" in a)
print("w" not in a)

print("W" in a)
print("W" not in a)

