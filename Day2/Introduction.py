# History of Python
# -----------------
# Python Implementation was started in December 1989 by Guido Van Rossum at Centrum Wiskunde & Informatica (CWI)  in Netherlands.
# The inspiration for the name came from the BBC’s TV Show – ‘Monty Python’s Flying Circus’, as he was a big fan of the TV show and also he wanted a short, unique and slightly mysterious name for his invention and hence he named it Python! 
# In February 1991, van Rossum published the code (labeled version 0.9.0).
# In 1994, Python 1.0 was released with new features like: lambda, map, filter, and reduce.
# Python 2.0 added new features like: list comprehensions, garbage collection system.
# On December 3, 2008, Python 3.0 (also called "Py3K") was released. 

# Python is influenced by
# 	Functional programming features from C
# 	Object Oriented Programming Features from C++
# 	Scripting Language Features from Perl and Shell Script
